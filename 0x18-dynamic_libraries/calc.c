#include "main.h"

/**
 * add - adding two numbers
 * @a: number one
 * @b: number two
 * Return: the result of the adding
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracting two numbers
 * @a: number one
 * @b: number two
 * Return: the result of the sub
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiblying two numbers
 * @a: number one
 * @b: number two
 * Return: the result of the multiblication
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division two numbers
 * @a: number one
 * @b: number two
 * Return: the result of the division
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod two numbers
 * @a: number one
 * @b: number two
 * Return: the result of the mod
 */

int mod(int a, int b)
{
	return (a % b);
}
