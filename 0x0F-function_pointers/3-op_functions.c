#include "3-calc.h"

/**
 * op-add - adding two number
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op-sub - subtracting two number
 * @a: first integer
 * @b: second integer
 *
 * Return: sub of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op-mul - multiblication of two number
 * @a: first integer
 * @b: second integer
 *
 * Return: mul of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op-div - division of two number
 * @a: first integer
 * @b: second integer
 *
 * Return: div of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op-mod - reminder of two number
 * @a: first integer
 * @b: second integer
 *
 * Return: mod of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
